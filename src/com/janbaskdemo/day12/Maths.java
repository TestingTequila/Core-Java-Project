package com.janbaskdemo.day12;

public class Maths {
    // WAP to add two integer numbers
    int x; //Global-Class Level Variable
    int y; //Global-Class Level Variable

    public void addition() {
        int sum = x + y;
        System.out.println("Sum of x and Y is : " + sum);
    }

    public void subtraction() {
        int diff = x - y;
        System.out.println("Difference of x and Y is : " + diff);
    }


}
